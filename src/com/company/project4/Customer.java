package com.company.project4;

public class Customer {

    String name;
    String phone;
    String sport;
    String team;
    Double gpa;
    String subject;
    int numFriends;
    String friends;

    public Customer(String name, String phone, String sport, String team, Double gpa, String subject, int numFriends, String friends) {
        this.name = name;
        this.phone = phone;
        this.sport = sport;
        this.team = team;
        this.gpa = gpa;
        this.subject = subject;
        this.numFriends = numFriends;
        this.friends = friends;
    }
}
